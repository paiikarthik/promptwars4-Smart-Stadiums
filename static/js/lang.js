function changeLanguage(lang) {
    if (!translations[lang]) return;
    
    // Save language setting locally
    localStorage.setItem("arena_lang", lang);

    document.querySelectorAll("[data-translate]").forEach(el => {
        const key = el.getAttribute("data-translate");
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Handle RTL layouts for Arabic
    if (lang === "ar") {
        document.body.style.direction = "rtl";
        document.body.style.textAlign = "right";
    } else {
        document.body.style.direction = "ltr";
        document.body.style.textAlign = "left";
    }
}

// Automatically apply selected language on page load
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("arena_lang") || "en";
    
    // Set language dropdown value if it exists
    const selector = document.getElementById("lang-selector");
    if (selector) {
        selector.value = savedLang;
    }
    
    changeLanguage(savedLang);
});
